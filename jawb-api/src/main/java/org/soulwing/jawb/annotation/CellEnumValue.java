package org.soulwing.jawb.annotation;

import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

/**
 * An annotation that is used to provide the textual value for an instance
 * of an enumerated type that appears in a workbook cell.
 *
 * @author Carl Harris
 */
@Target(ElementType.FIELD)
@Retention(RetentionPolicy.RUNTIME)
public @interface CellEnumValue {

  String value();
  
}
